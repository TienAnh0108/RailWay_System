package Station;

import Station.Station;

public class Station {
    private int id;
    private String name;

    public Station() {}

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
